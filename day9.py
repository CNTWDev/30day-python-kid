# 作业目标：
  # - 巩固字符串基本操作。
  # - 练习安全地读取字符串中的字符。
# 作业要求：
  # 1. 输入学生的名字，输出名字长度。
  # 2. 输出名字的第一个字符和最后一个字符（先检查长度）。
  # 3. 额外挑战：把名字重复三次打印出来。

# ================================
# 作业参考答案 (Reference Solution)
# ================================

def homework_solution(name: str) -> None:
    """输出名字相关信息，包括长度和首尾字符。"""
    if not isinstance(name, str):
        raise TypeError("name 需要是字符串。")
    cleaned = name.strip()
    length = len(cleaned)
    print("名字是:", cleaned)
    print("名字长度:", length)
    if length == 0:
        print("名字不能为空哦！")
        return
    print("第一个字符:", cleaned[0])
    print("最后一个字符:", cleaned[-1])
    print("名字重复三次:", cleaned * 3)

# 供教师演示或学生验证使用
if __name__ == "__main__":
    homework_solution('星星')
