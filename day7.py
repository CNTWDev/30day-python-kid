# 示例 1：基础 while 循环
count = 1
while count <= 5:
    print("第", count, "次问好！")
    count += 1

# 示例 2：提早结束循环
candies = 0
while candies < 10:
    candies += 2
    print("我有", candies, "颗糖。")
    if candies >= 6:
        print("满足啦，停止收集！")
        break

