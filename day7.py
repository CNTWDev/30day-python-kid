  # 2. 额外挑战：当倒计时到 3 时，打印 "准备好了吗？"。

# ================================
# 作业参考答案 (Reference Solution)
# ================================

def homework_solution(start: int) -> None:
    """进行倒计时，从 start 倒数到 1。"""
    if not isinstance(start, int):
        raise TypeError("start 需要是整数。")
    if start < 1:
        raise ValueError("start 需要大于等于 1。")
    current = start
    while current >= 1:
        print(current)
        if current == 3:
            print("准备好了吗？")
        current -= 1
    print("出发！")

# 供教师演示或学生验证使用
if __name__ == "__main__":
    homework_solution(start=5)
