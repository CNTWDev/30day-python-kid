def homework_solution(friend_name: str, age: int) -> None:
    """输出欢迎消息、朋友信息和算术结果。"""
    print("欢迎来到我的小程序！")
    print("我的好朋友是", friend_name, "他今年", age, "岁。")
    print("我来做数学题啦！")
    print("12 + 8 - 3 =", 12 + 8 - 3)


# 供教师演示或学生验证使用
if __name__ == "__main__":
    # 将名字和年龄替换成孩子自己的朋友信息
    homework_solution(friend_name="小明", age=8)

