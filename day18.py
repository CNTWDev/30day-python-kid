def say_hello(name: str) -> None:
    print("你好,", name, "！欢迎来到函数世界！")

say_hello("小天")

# 示例 2：带返回值
def add_two_numbers(a: int, b: int) -> int:
    return a + b

result = add_two_numbers(3, 4)
print("3 + 4 =", result)

