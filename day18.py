  # 2. 函数返回面积，并在外部打印。
  # 3. 输入验证：长宽必须为正数。
  # 4. 额外挑战：当长宽相等时提示这是正方形。

# ================================
# 作业参考答案 (Reference Solution)
# ================================

def homework_solution(length: float, width: float) -> float:
    """计算矩形面积并打印说明。"""
    for value, name in ((length, "length"), (width, "width")):
        if not isinstance(value, (int, float)):
            raise TypeError(f"{name} 需要是数字。")
        if value <= 0:
            raise ValueError(f"{name} 需要是正数。")
    area = length * width
    print(f"矩形面积为 {area} 平方单位。")
    if length == width:
        print("提示：这是一个正方形！")
    return area

